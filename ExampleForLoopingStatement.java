public class ExampleForLoopingStatement {

	public static void main(String[] args) {
		
		for(int i=1;i<=5;i++)
		{
			System.out.println("Hello");
		}
        
		System.out.println("--------------------------------");
		
		for(int i=1;i<=5;i++)
		{
			System.out.println(i);
		}
		
        System.out.println("--------------------------------");
		
		for(int i=5;i>=1;i--)
		{
			System.out.println(i);
		}
		
        System.out.println("--------------------------------");
		
		for(int i=2;i<=10;i+=2)
		{
			System.out.println(i);
		}
		
        System.out.println("--------------------------------");
		
		for(int i=1;i<=5;i+=2)
		{
			System.out.println(i);
		}

	}

}
